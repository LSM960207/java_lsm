package day2;

public class Ex5_Comparison {

	public static void main(String[] args) {
		/* >= 반대로 쓰면 안됨
		 * <= 반대로 쓰면 안됨
		 * ==를 =과 혼동하면 안됨
		 * 결과 값이 참 또는 거짓
		 * 비교 연산자는 연산자를 기준으로 양쪽에 어떤 수가 나옴
		 */
		int score = 95;
		System.out.println(score > 95);
		System.out.println(score >= 95);
		System.out.println(score < 95);
		System.out.println(score <= 95);
		System.out.println(score != 95);

	}

}
